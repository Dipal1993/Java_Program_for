//write a program that prints the sum of x ranging from 1 to 20.

public class sum_of_x_range {

    public static void main(String[] args) {

        int sum =0;
        for(int i =1;i<=20;i++){

            sum = sum +i;
        }
        System.out.println("Sum of 1 to 20 number is : "+sum);
    }
}
